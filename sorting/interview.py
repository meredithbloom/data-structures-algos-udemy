# merge sort - not sorting in memory because data is so vast, but size of data is very large so we are concerned with time complexity

#5 - Almost sorted Udemy review data needs to update and add 2 new reviews
# insertion sort - input is small and mostly sorted

#6 - Temperature Records for the past 50 years in Canada
# radix or counting - working with integers (assuming only whole numbers) of fixed range
# quick sort if temperatures are very precise with decimals

#7 - Large user name database needs to be sorted. Data is very random.
# merge sort - if we have enough memory
# quick sort - if not too worried about worst case and we pick a good pivot

